public class Swapping2 {
    public static void main(String[] args)
    {
        int a=6;
        int b=4;
        System.out.println("value of a is" +a);
        System.out.println("Value of b is" +b);
        int temp=a;
        a=b;
        b=temp;
        System.out.println("After Swapping");
        System.out.println("Value of a is" +a);
        System.out.println("Value of b is" +b);
    }
}