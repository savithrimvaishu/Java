public class Program3 {
    public static void main(String[] args)
    {
        int item=2;
        switch(item)
        {
            case 1:
            System.out.println("Black Coffee");
            break;
            case 2:
            System.out.println("Green Tea");
            break;
            case 3:
            System.out.println("Lemon Tea");
            break;
            case 4:
            System.out.println("Ginger Tea");
            break;
            default:
            System.out.println("INVALID");

        }

    }
}